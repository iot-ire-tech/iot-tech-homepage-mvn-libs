package com.models.stripe.entities;

/**
 *
 * @author ennisa
 */
public class EntDob {

	int year = 1972;
	int month = 2;
	int day = 1;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

}
